package com.pojo;

public class Messages {
	private String Number;
	private String Text;

	public final String getNumber() {
		return Number;
	}

	public final void setNumber(String number) {
		Number = number;
	}

	public final String getText() {
		return Text;
	}

	public final void setText(String text) {
		Text = text;
	}

}

